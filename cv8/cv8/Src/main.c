void uart_byte_available(uint8_t c)
{
    static uint16_t cnt;
    static char data[CMD_BUFFER_LEN];

    if ((HAL_GetTick() - timeout) >= 2000)
       cnt = 0;

    if (cnt < CMD_BUFFER_LEN) data[cnt++] = c;

    if (c == '\n' || c == '\r') {
        data[cnt] = '\0';
        uart_process_command(data);
        cnt = 0;
    }


}

void uart_process_command(char const *cmd) {
    char cmd_buffer[CMD_BUFFER_LEN];
    int i_data = 0;
    int i_buffer = 0;
    char c;

    do {
        c = cmd[i_data];
        if ((c >= 32 && c <= 126) || c == '\0') {
            cmd_buffer[i_buffer] = cmd[i_data];
            i_buffer++;
        }
        i_data++;
    } while(c != '\0');

    char *token;
    token = strtok(cmd_buffer, " ");

    if (strcasecmp(token, "HELLO") == 0) {
        printf("Komunikace OK\n");
    }

    else if (strcasecmp(token, "SET") == 0) {
       token = strtok(NULL, " ");

       if (strcasecmp(token, "RED") == 0) {
            printf("Cervena\n");
            token = strtok(NULL, " ");

           if (strcasecmp(token, "ON") == 0) {
                printf("Zapnout\n");
                HAL_GPIO_WritePin(LD5_GPIO_Port, LD5_Pin, 1);
            }

            else if (strcasecmp(token, "OFF") == 0) {
                printf("Vypnou\n");
                HAL_GPIO_WritePin(LD5_GPIO_Port, LD5_Pin, 0);
            }

        }

        else if (strcasecmp(token, "BLUE") == 0) {
            printf("Modra\n");
            token = strtok(NULL, " ");

           if (strcasecmp(token, "ON") == 0) {
                printf("Zapnout\n");
                HAL_GPIO_WritePin(LD6_GPIO_Port, LD6_Pin, 1);
            }

            else if (strcasecmp(token, "OFF") == 0) {
                printf("Vypnou\n");
                HAL_GPIO_WritePin(LD6_GPIO_Port, LD6_Pin, 0);
            }
        }

        else if (strcasecmp(token, "GREEN") == 0) {
            printf("Zelena\n");
            token = strtok(NULL, " ");

           if (strcasecmp(token, "ON") == 0) {
                printf("Zapnout\n");
                HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, 1);
            }

            else if (strcasecmp(token, "OFF") == 0) {
                printf("Vypnou\n");
                HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, 0);
            }
        }

        else if (strcasecmp(token, "ORANGE") == 0) {
            printf("Oranzova\n");
            token = strtok(NULL, " ");

           if (strcasecmp(token, "ON") == 0) {
                printf("Zapnout\n");
                HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, 1);
            }

            else if (strcasecmp(token, "OFF") == 0) {
                printf("Vypnou\n");
                HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, 0);
            }
        }
    }

    else if (strcasecmp(token, "READ") == 0) {
        printf("Oranzova: %d \n", HAL_GPIO_ReadPin(LD3_GPIO_Port, LD3_Pin));
        printf("Zelena: %d \n", HAL_GPIO_ReadPin(LD4_GPIO_Port, LD4_Pin));
        printf("Modra: %d \n", HAL_GPIO_ReadPin(LD6_GPIO_Port, LD6_Pin));
        printf("Cervena: %d \n", HAL_GPIO_ReadPin(LD5_GPIO_Port, LD5_Pin));
    }

    else if (strcasecmp(token, "VOLTAGE") == 0) {
        float voltage;

        HAL_ADC_Start(&hadc1);
        HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
        uint32_t value = HAL_ADC_GetValue(&hadc1);
        HAL_ADC_Stop(&hadc1);

        voltage = (1210*4096)/value;
        printf("ADC value: %d\n", value);
        printf("voltage: %5.2f\n", voltage);
    }

    printf("prijato: '%s'\n", cmd_buffer);
}

