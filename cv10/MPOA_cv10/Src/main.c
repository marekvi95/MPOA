/* StartVisualTask function */
void StartVisualTask(void const * argument)
{
  /* USER CODE BEGIN StartVisualTask */
  /* Infinite loop */
  uint16_t msg;

  for(;;)
  {
    if (xQueueReceive(xVisualQueueHandle, &msg, portMAX_DELAY)) {
        HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, (msg & 0x01) ? GPIO_PIN_SET : GPIO_PIN_RESET);
        HAL_GPIO_WritePin(LD4_GPIO_Port, LD4_Pin, (msg & 0x02) ? GPIO_PIN_SET : GPIO_PIN_RESET);
        HAL_GPIO_WritePin(LD5_GPIO_Port, LD5_Pin, (msg & 0x04) ? GPIO_PIN_SET : GPIO_PIN_RESET);
        HAL_GPIO_WritePin(LD6_GPIO_Port, LD6_Pin, (msg & 0x08) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    }
    osDelay(1);
  }
  /* USER CODE END StartVisualTask */
}

/* StartDecisionTask function */
void StartDecisionTask(void const * argument)
{
  /* USER CODE BEGIN StartDecisionTask */
  /* Infinite loop */
  for(;;)
  {
    uint32_t msg_in;

    if (xQueueReceive(xDecisionQueueHandle, &msg_in, portMAX_DELAY)) {
        int16_t accX = msg_in >> 0;
        int16_t accY = msg_in >> 16;
        uint16_t msg_out = 0;

        if (accX < -200) {
            msg_out |= 0x02;
        }
        else if (accX > 200) {
            msg_out |= 0x04;
        }

        if (accY < -200) {
            msg_out |= 0x08;
        }
        else if (accY > 200) {
            msg_out |= 0x01;
        }

        xQueueSend(xVisualQueueHandle, &msg_out, 0);

    }
    osDelay(1);
  }
  /* USER CODE END StartDecisionTask */
}

/* StartAcceleroTask function */
void StartAcceleroTask(void const * argument)
{
  /* USER CODE BEGIN StartAcceleroTask */
  BSP_ACCELERO_Init();
  int16_t buffer[3] = {0};
  uint16_t xval, yval = 0x00;
  uint32_t msg_out;
  /* Infinite loop */
  for(;;)
  {
    /* Accelerometer variables */

    /* Read Acceleration */
    BSP_ACCELERO_GetXYZ(buffer);

    xval = buffer[0];
    yval = buffer[1];

    msg_out = (uint32_t)yval << 16 | (xval);
    xQueueSend(xDecisionQueueHandle, &msg_out, 0);

    osDelay(50);
  }
  /* USER CODE END StartAcceleroTask */
}

